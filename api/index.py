async def process_recommendation(query):
    try:
        response = GRADIO_CLIENT.predict(
            query=query,
            history=[],
            system=""" based on the user input suggest the products from amazone  avilable in the market  not tht i need the product name  return in json  example catagory: [
    "products":[{
        product_name: ,
        short_discription: 
      }
  ] note that json should start ```json\n(.*?)``` """,
            api_name="/model_chat_1"
        )
        
        raw_response = response[1][0][1]
        json_match = re.search(r'```json\n(.*?)```', raw_response, re.DOTALL) or \
                    re.search(r'{.*}', raw_response, re.DOTALL)
        
        if not json_match:
            return []
            
        json_part = json_match.group(1) or json_match.group(0)
        parsed_data = json.loads(json_part)
        products = extract_products(parsed_data)
        
        if not products:
            return []

        async with aiohttp.ClientSession() as session:
            tasks = [fetch_product_info(session, p) for p in products]
            return await asyncio.gather(*tasks)

    except Exception as e:
        print(f"Processing error: {str(e)}")
        return []

def extract_products(json_data):
    if "products" in json_data:
        return json_data["products"]
    products = []
    if "category" in json_data:
        for category in json_data["category"]:
            products.extend(category["products"] if "products" in category else category)
    return products

