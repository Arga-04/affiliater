async def fetch_product_details(url):
    """Improved product details fetcher with proper headers and error handling"""
    try:
        headers = {
            "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/91.0.4472.124 Safari/537.36",
            "Accept-Language": "en-US,en;q=0.9",
        }
        
        async with aiohttp.ClientSession(headers=headers) as session:
            async with session.get(url) as response:
                if response.status == 200:
                    content = await response.text()
                    soup = bs(content, "html.parser")
                    
                    # Extract main product details
                    product_data = {
                        "images": [],
                        "description": "",
                        "title": ""
                    }

                    # Extract title
                    title_tag = soup.find("span", {"id": "productTitle"})
                    if title_tag:
                        product_data["title"] = title_tag.get_text(strip=True)

                    # Extract images
                    image_containers = soup.find_all("div", class_=re.compile(r"imgTagWrapper"))
                    for container in image_containers:
                        img = container.find("img")
                        if img and img.get("src"):
                            product_data["images"].append({
                                "url": img["src"],
                                "alt": img.get("alt", "")
                            })

                    # Extract description
                    description_div = soup.find("div", {"id": "productDescription"})
                    if description_div:
                        product_data["description"] = description_div.get_text(strip=True)

                    return product_data
                
                return None

    except Exception as e:
        print(f"Error fetching product details: {str(e)}")
        return None

async def fetch_product_info(session, product):
    """Improved product info fetcher with error handling"""
    try:
        api_key = random.choice(API_KEYS)
        data = {
            "api_key": api_key,
            "query": f"{product['product_name']} give link with asin",
            "search_depth": "basic",
            "include_domains": ["amazon.in"],
            "max_results": 1
        }

        async with session.post(TAVILY_API_URL, json=data, headers=TAVILY_HEADERS) as response:
            if response.status == 200:
                result = await response.json()
                if result.get('results'):
                    product_link = result['results'][0]['url']
                    asin = re.search(r"/dp/([A-Z0-9]+)", product_link)
                    asin = asin.group(1) if asin else None
                    
                    # Get detailed product info
                    details = await fetch_product_details(product_link)
                    
                    return {
                        **product,
                        "product_link": product_link,
                        "asin": asin,
                        "affiliate_link": f"https://www.amazon.in/dp/{asin}/?tag=lumeth2023-21" if asin else None,
                        "details": details
                    }
    except Exception as e:
        print(f"Error processing {product['product_name']}: {str(e)}")
    return None

